  
public class Employee {
	
	   String name;
	   int id;
	   double basicSalary;
	   
	   
	   
	   
	public Employee(String name, int id, double basicSalary) {
		this.name = name;
		this.id = id;
		this.basicSalary = basicSalary;
		
	}
	
	public double calculateSalary(){
		
		double salary = basicSalary;
		return salary;
		
	}
	   
	   
	   
	   

}
